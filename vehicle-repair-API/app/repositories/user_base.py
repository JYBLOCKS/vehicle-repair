from typing import Protocol, Sequence, Any
from uuid import UUID

class UserRepository(Protocol):
    async def create(self, *, email: str, name: str, password:str) -> Any: ...
    async def get_by_id(self, user_id: UUID) -> Any | None: ...
    async def get_by_email(self, email: str) -> Any | None: ...
    async def list(self, limit: int = 50, offset: int = 0) -> Sequence[Any]: ...
    async def update_name(self, user_id: UUID, name: str) -> Any | None: ...
    async def delete(self, user_id: UUID) -> None: ...
