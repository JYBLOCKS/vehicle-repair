from typing import Protocol, Sequence, Any
from uuid import UUID


class EstimateRepository(Protocol):
    async def create(self, **data) -> Any: ...
    async def get_by_id(self, estimate_id: UUID) -> Any | None: ...
    async def list(self, limit: int = 50, offset: int = 0) -> Sequence[Any]: ...
    async def update(self, estimate_id: UUID, **data) -> Any | None: ...
    async def delete(self, estimate_id: UUID) -> None: ...

